constexpr int N = 5e6 + 10; 
int n, m;

int pri[N], check[N], mu[N];
int M[N];
void sieve() {
	memset(check, 0, sizeof check);
	*pri = 0;
	mu[1] = 1;
	for (int i = 2; i < N; ++i) {
		if (!check[i]) {
			pri[++*pri] = i;
			mu[i] = -1;
		}
		for (int j = 1; j <= *pri; ++j) {
			if (1ll * i * pri[j] >= N) break;
			check[i * pri[j]] = 1;
			if (i % pri[j] == 0) {
				mu[i * pri[j]] = 0;
				break;
			} else {
				mu[i * pri[j]] = -mu[i];
			}
		}
	}
	M[0] = 0;
	for (int i = 1; i < N; ++i) M[i] += M[i - 1] + mu[i];
}

void run() {
	rd(n, m);
	ll tot = 0;
	if (n < m) swap(n, m);
	for (int i = 1, j; i <= m; i = j + 1) {
		j = min(n / (n / i), m / (m / i));
		tot += 1ll * (M[j] - M[i - 1]) * (n / i) * (m / i);
	}
	pt(tot * 4 + 4);	
}

