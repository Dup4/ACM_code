constexpr int N = 2e5 + 10;
int n, a[N];
ll P, K;
void run() {
    cin >> n >> P >> K;
    for (int i = 1; i <= n; ++i) cin >> a[i];
    sort(a + 1, a + 1 + n);
    vector<vector<int>> vec, S;
    vec.clear();
    vec.resize(K + 1);
    S.clear();
    S.resize(K + 1);
    for (auto &it : vec) it.push_back(0);
    for (auto &it : S) it.push_back(0);
    for (int i = K; i <= n; ++i) {
        vec[i % K].push_back(a[i]);
        S[i % K].push_back(S[i % K].back() + vec[i % K].back());
    }
    int res = 0;
    int now = P;
    for (int i = 0; i < K; ++i) {
        now -= a[i];
        int pos = upper_bound(S[i].begin(), S[i].end(), now) - S[i].begin();
        --pos;
        chmax(res, i + pos * K);
    }
    pt(res);
}

