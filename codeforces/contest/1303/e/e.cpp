constexpr int N = 4e2 + 10;
int f[N][N], ls, lt;
char s[N], t[N];

bool gao(int pos) {
    int x = pos, y = lt - pos;
    memset(f, -1, sizeof f);
    f[0][0] = 0;
    for (int i = 1; i <= ls; ++i) {
        for (int j = x; j >= 0; --j) {
            chmax(f[i][j], f[i - 1][j]);
            if (f[i][j] == y)
                continue;
            if (f[i][j] != -1 && s[i] == t[pos + f[i][j] + 1])
                ++f[i][j];
            if (j && s[i] == t[j])
                chmax(f[i][j], f[i - 1][j - 1]);
        }
        if (f[i][x] == y)
            return true;
    }
    return false;
}

void run() {
    cin >> (s + 1) >> (t + 1);
    ls = strlen(s + 1), lt = strlen(t + 1);
    for (int i = 1; i <= lt; ++i) {
        if (gao(i)) {
            return pt("YES");
        }
    }
    pt("NO");
}

