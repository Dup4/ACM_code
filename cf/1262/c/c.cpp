constexpr int N = 2e3 + 10;
int n, k; string s; 
void run() {
	cin >> n >> k >> s;
	vector <pII> vec;
	for (int i = 0; i < n; ++i) {
		char now = (i & 1) ? ')' : '(';
		if (s[i] == now) continue;
		for (int j = i + 1; j < n; ++j) {
			if (s[j] == now) {
				vec.push_back(pII(i + 1, j + 1));
				reverse(s.begin() + i, s.begin() + j + 1); 
			    break;	
			}
		}
	}
	int del = n / 2 - k;
	for (int i = 2, j = 1; i < n && del; i += 2, ++j, --del) {
		vec.push_back(pII(j + 1, i + 1));
	}
	pt(vec.size());
	for (auto &it : vec)
		pt(it.fi, it.se);
}

