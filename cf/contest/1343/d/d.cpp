constexpr int N = 4e5 + 10; 
int n, K, a[N], b[N]; 

void add(int l, int r, int v) {
	if (l > r) return;
	b[l] += v;
	b[r + 1] -= v;
}

void run() {
	rd(n, K);
	for (int i = 0; i <= K * 2 + 5; ++i) b[i] = 0;
	for (int i = 1; i <= n; ++i) rd(a[i]);
	for (int i = 1; i <= n / 2; ++i) {
		int nx = n - i + 1;
		int A = a[i], B = a[nx];
		if (A > B) swap(A, B);
		add(2, A, 2);
		add(B + K + 1, K * 2, 2);
		add(A + 1, B + K, 1);
		add(A + B, A + B, -1);
	}
	int res = 1e9;
	for (int i = 2; i <= K * 2; ++i) {
		b[i] += b[i - 1];
		chmin(res, b[i]);
	}
	pt(res);

}

