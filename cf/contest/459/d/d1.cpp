constexpr int N = 1e6 + 10;
int n, m, a[N], b[N], cnt[N], f[N];
tree<pII, null_type, less<pII>, rb_tree_tag, tree_order_statistics_node_update> rbtree;

void run() {
    rbtree.clear();
    for (int i = 1; i <= n; ++i) {
        cin >> a[i];
        b[i] = a[i];
    }
    sort(b + 1, b + 1 + n);
    m = unique(b + 1, b + 1 + n) - b - 1;
    for (int i = 1; i <= n; ++i) {
        a[i] = lower_bound(b + 1, b + 1 + m, a[i]) - b;
    }
    for (int i = 1; i <= n; ++i) {
        ++cnt[a[i]];
        f[i] = cnt[a[i]];
    }
    memset(cnt, 0, sizeof cnt);
    ll res = 0;
    for (int i = n; i >= 1; --i) {
        ++cnt[a[i]];
        res += rbtree.order_of_key(pII(f[i], 0));
        rbtree.insert(pII(cnt[a[i]], i));
    }
    pt(res);
}

