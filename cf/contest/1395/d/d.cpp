constexpr int N = 1e5 + 10;
int n, d;
ll m, a[N];

void run() {
    rd(n, d, m);
    for (int i = 1; i <= n; ++i) rd(a[i]);
    vector<ll> vec[2];
    for (int i = 1; i <= n; ++i) {
        vec[a[i] > m].push_back(a[i]);
    }
    for (auto &it : {0, 1})
        if (SZ(vec[it])) {
            sort(all(vec[it]));
            reverse(all(vec[it]));
            for (int i = 1; i < SZ(vec[it]); ++i) vec[it][i] += vec[it][i - 1];
        }
    ll res = 0;
    if (SZ(vec[0]))
        res = vec[0].back();
    for (int i = 0; i < SZ(vec[1]); ++i) {
        ll useD = 1ll * i * (d + 1) + 1;
        ll now = 0;
        if (SZ(vec[0]) && useD < n) {
            now += vec[0][min(1ll * SZ(vec[0]), n - useD) - 1];
        }
        if (useD <= n) {
            now += vec[1][i];
            chmax(res, now);
        }
    }
    pt(res);
}

