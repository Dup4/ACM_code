constexpr int N = 1e7 + 10;
int n, p, x, y, z, b[N], a[N];
ll f[N], g[N];

void run() {
    rd(n, p, x, y, z, b[1]);
    b[0] = -1e9;
    f[0] = g[0] = 0;
    f[1] = b[1];
    g[1] = b[1];
    *a = 0;
    a[++*a] = 0;
    a[++*a] = 1;
    ll res = f[1];
    for (int i = 2; i <= n; ++i) {
        b[i] = (1ll * x * f[i - 1] % p + 1ll * y * b[i - 1] % p + z) % p;
        while (*a && b[i] <= b[a[*a]]) --*a;
        //	dbg(i, f[i]);
        //	dbg(a[*a]);
        g[i] = g[a[*a]] + 1ll * (i - a[*a]) * b[i] % mod;
        f[i] = f[i - 1] + g[i];
        g[i] %= mod;
        f[i] %= mod;
        res ^= f[i];
        //	dbg(i, f[i], b[i], g[i]);
        a[++*a] = i;
    }
    pt(res);
}

